#pragma once

#include <Arduino.h>

class Rectangle {
 public:
  //------------------------------------------------------------------------------
  Rectangle(double width, double height)
      : width_(width),
        height_(height)
  //------------------------------------------------------------------------------
  {}

  //------------------------------------------------------------------------------
  double width() const
  //------------------------------------------------------------------------------
  {
    return width_;
  }

  //------------------------------------------------------------------------------
  double height() const
  //------------------------------------------------------------------------------
  {
    return height_;
  }

 private:
  double width_;
  double height_;
};
