#pragma once

#include <Arduino.h>

class Position {
 public:
  //------------------------------------------------------------------------------
  Position(double x, double y)
      : x_(x),
        y_(y)
  //------------------------------------------------------------------------------
  {}

  //------------------------------------------------------------------------------
  double getX() const
  //------------------------------------------------------------------------------
  {
    return x_;
  }

  //------------------------------------------------------------------------------
  double getY() const
  //------------------------------------------------------------------------------
  {
    return y_;
  }

 private:
  double x_;
  double y_;
};