#pragma once

#include <Arduino.h>

class Picture {
 public:
  //------------------------------------------------------------------------------
  Picture()
      : width_(0.0),
        height_(0.0),
        overlapWidth_(0.0),
        overlapHeight_(0.0)
  //------------------------------------------------------------------------------
  {}

  //------------------------------------------------------------------------------
  Picture(double width, double height, double overlapWidth, double overlapHeight)
      : width_(width),
        height_(height),
        overlapWidth_(overlapWidth),
        overlapHeight_(overlapHeight)
  //------------------------------------------------------------------------------
  {}

  //------------------------------------------------------------------------------
  double getWidth() const
  //------------------------------------------------------------------------------
  {
    return width_;
  }

  //------------------------------------------------------------------------------
  double getHeight() const
  //------------------------------------------------------------------------------
  {
    return height_;
  }

  //------------------------------------------------------------------------------
  double getWidthWithinPano() const
  //------------------------------------------------------------------------------
  {
    return width_ - overlapWidth_;
  }

  //------------------------------------------------------------------------------
  double getHeightWithinPano() const
  //------------------------------------------------------------------------------
  {
    return height_ - overlapHeight_;
  }

  //------------------------------------------------------------------------------
  double getOverlapWidth() const
  //------------------------------------------------------------------------------
  {
    return overlapWidth_;
  }

  //------------------------------------------------------------------------------
  double getOverlapHeight() const
  //------------------------------------------------------------------------------
  {
    return overlapHeight_;
  }

 private:
  double width_;
  double height_;
  double overlapWidth_;
  double overlapHeight_;
};